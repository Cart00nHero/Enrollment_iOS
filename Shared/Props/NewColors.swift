    return Color("FlameScarlet").opacity(opacity)
}
func golden(_ opacity: Double) -> Color {
    return Color("Golden").opacity(opacity)
}
func skyBlue(_ opacity: Double) -> Color {
    return Color("SkyBlue").opacity(opacity)
}
func blueSea(_ opacity: Double) -> Color {
    return Color("BlueSea").opacity(opacity)
}
